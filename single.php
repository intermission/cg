		<p>What else can I say about the <a href="#">United States Barista Championship</a> that hasn't been said already - it rocked!</p>

<p>I was totally pumped to go to back to Portland, Oregon. The last time I was there was in 2003 for the (now defunct) NASCOR tradeshow when it hosted the North West Regional Barista Competition. Coffee and competitions have come such a long way since then. The 2009 USBC had a dedicated media room, stadium seating, live internet broadcast, live chat, competitor interviews posted almost instantly, a fashion review and way more tattoos than ever!</p>

<p>Competitions are a really unique gathering of some of the most talented baristas in the country. I am always inspired, excited and re-energized about the barista craft and the exciting possibilities within coffee and the industry.</p>

<p>The drive from Vancouver, Canada seemed to take forever. But a stop at Greenlake Zoka Coffee House location in Seattle helped break up the trip and provide much needed espresso. There were so many places on our list to stop, but time was tight. Zoka always tops my list. The barista there, without question, was passionate enough about their craft to redo the espresso until perfection. And I indulged in one of their amazing scones. A new fan of Zoka Coffee is my three year old boy, Sal.</p>

<p>I really looked forward to seeing some of my old Portland friends compete. Phoung Tran (of Lava Java, and the 2005 USBC champion) is an awesome barista and it was great to see her back in competition. Billy Wilson is also great to see, although he's been extra busy lately. He became a new father to a baby girl and also opened BARISTA, his new cafe located in Portland in the same week. He opted out of the competition this year because of these major events. Understandable. Hope to see you back on stage soon, Billy.</p>

<p>At BARISTA Billy currently serves coffee from Intelligentsia, Stumptown and Ecco Caffe. I had a good shot there every time I went and he had an awesome siphon set up. I had a great shot of single origin Brasil roasted by Ecco, I had a nice Ethiopian on the Syphon roasted by Intelligentsia, and an excellent shot of Stumptown's Hair Bender.</p>
	
